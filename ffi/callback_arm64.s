// callbackDispatcher handles the actual callback invocation.
// Called from each trampoline entry via BL.
TEXT ·callbackDispatcher(SB), NOSPLIT|NOFRAME, $0
	// Save LR which contains the return address from the trampoline
	// X30 (LR) was set by BL instruction, pointing to the next trampoline entry
	MOVD R30, R19            // R19 = trampoline return address (callee-saved)

	// Pop the original caller's return address from stack
	// (C code pushed it when calling our callback)
	MOVD 0(RSP), R20         // R20 = original caller's return address (callee-saved)
	ADD  $8, RSP, RSP        // Remove return address from stack

	// Allocate space for saved registers
	// ARM64 AAPCS64: D0-D7 (8 floats * 8 = 64 bytes) + X0-X7 (8 ints * 8 = 64 bytes) = 128 bytes
	SUB  $128, RSP, RSP

	// Save floating-point registers D0-D7
	FMOVD F0, 0(RSP)
	FMOVD F1, 8(RSP)
	FMOVD F2, 16(RSP)
	FMOVD F3, 24(RSP)
	FMOVD F4, 32(RSP)
	FMOVD F5, 40(RSP)
	FMOVD F6, 48(RSP)
	FMOVD F7, 56(RSP)

	// Save integer registers X0-X7
	MOVD R0, 64(RSP)
	MOVD R1, 72(RSP)
	MOVD R2, 80(RSP)
	MOVD R3, 88(RSP)
	MOVD R4, 96(RSP)
	MOVD R5, 104(RSP)
	MOVD R6, 112(RSP)
	MOVD R7, 120(RSP)

	// Calculate callback index
	// index = (return_address - trampoline_base) / 4 - 1
	MOVD $·callbackTrampoline(SB), R0
	SUB  R0, R19, R0         // R0 = offset from callbackTrampoline
	MOVD $4, R1              // Each BL is 4 bytes
	UDIV R1, R0, R0          // R0 = R0 / 4
	SUB  $1, R0, R0          // Adjust for BL pointing to next instruction

	// Allocate space for callbackArgs struct + alignment
	// struct { index uintptr; args unsafe.Pointer; result uintptr }
	SUB  $32, RSP, RSP       // 24 bytes for struct + 8 for alignment

	// Initialize callbackArgs
	MOVD R0, 0(RSP)          // callbackArgs.index
	ADD  $32, RSP, R1        // R1 = address of saved registers
	MOVD R1, 8(RSP)          // callbackArgs.args
	MOVD $0, R0
	MOVD R0, 16(RSP)         // callbackArgs.result = 0

	// Call Go function: callbackWrap(*callbackArgs)
	MOVD RSP, R0             // R0 = &callbackArgs (first argument)
	BL   ·callbackWrap(SB)

	// Retrieve result
	MOVD 16(RSP), R0         // R0 = callbackArgs.result

	// Clean up callbackArgs
	ADD  $32, RSP, RSP

	// Restore stack (skip saved registers)
	ADD  $128, RSP, RSP

	// Return to original caller
	MOVD R20, R30            // Restore original LR
	RET
